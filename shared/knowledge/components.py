def find_by_mpn(mpn: str) -> ComponentEntry | None:
    """Look up a component by MPN (case-insensitive)."""
    key = mpn.upper().replace("-", "").replace("+", "P")
    for entry in COMPONENTS:
        candidate = entry.get("mpn", "").upper().replace("-", "").replace("+", "P")
        if candidate == key:
            return entry
    return None


def find_by_category(category: str) -> list[ComponentEntry]:
    """Return all components in the given category."""
    return [e for e in COMPONENTS if e.get("category") == category]


def find_by_interface(interface: str) -> list[ComponentEntry]:
    """Return all components supporting the given interface type."""
    iface = interface.upper()
    return [e for e in COMPONENTS if iface in [i.upper() for i in e.get("interface_types", [])]]
