def _stm32_impl(cfg: SafetyConfig) -> str:
    lines = [
        "/* boardsmith-fw safety monitor — STM32/HAL */",
        '#include "safety_config.h"',
        '#include "stm32f4xx_hal.h"',
        "",
    ]

    if cfg.watchdog.enabled:
        lines += [
            "#if SAFETY_WDT_ENABLED",
            "static IWDG_HandleTypeDef hiwdg;",
            "#endif",
            "",
        ]

    lines += [
        "static uint32_t last_stack_check;",
        "static uint32_t last_heap_check;",
        "",
    ]

    # Stack painting helpers
    if cfg.stack.enabled:
        lines += [
            "/* Linker symbols — set in linker script */",
            "extern uint32_t _estack;",
            "extern uint32_t _Min_Stack_Size;",
            "",
            "static uint32_t stack_check_free(void) {",
            "    volatile uint32_t *bottom = &_estack - (uint32_t)(uintptr_t)&_Min_Stack_Size;",
            "    uint32_t free_words = 0;",
            "    while (*bottom == SAFETY_STACK_PAINT) {",
            "        free_words++;",
            "        bottom++;",
            "    }",
            "    return free_words * 4;",
            "}",
            "",
        ]

    # safety_init
    lines += [
        "void safety_init(void) {",
    ]

    if cfg.watchdog.enabled:
        # IWDG prescaler/reload: LSI ~32kHz, prescaler 64 → 500Hz tick
        # reload = timeout_ms * 500 / 1000
        reload_val = min(4095, max(1, cfg.watchdog.timeout_ms * 500 // 1000))
        lines += [
            "#if SAFETY_WDT_ENABLED",
            "    hiwdg.Instance = IWDG;",
            "    hiwdg.Init.Prescaler = IWDG_PRESCALER_64;",
            f"    hiwdg.Init.Reload = {reload_val};",
            "    HAL_IWDG_Init(&hiwdg);",
            "#endif",
        ]

    lines += [
        "    last_stack_check = 0;",
        "    last_heap_check = 0;",
        "}",
        "",
    ]

    # safety_wdt_feed
    lines += [
        "void safety_wdt_feed(void) {",
        "#if SAFETY_WDT_ENABLED",
        "    HAL_IWDG_Refresh(&hiwdg);",
        "#endif",
        "}",
        "",
    ]

    # safety_check — called from main super-loop
    lines += [
        "void safety_check(uint32_t now_ms) {",
    ]

    if cfg.stack.enabled:
        lines += [
            "#if SAFETY_STACK_ENABLED",
            "    if (now_ms - last_stack_check >= SAFETY_STACK_CHECK_MS) {",
            "        last_stack_check = now_ms;",
            "        uint32_t free = stack_check_free();",
            "        if (free < SAFETY_STACK_MIN_FREE) {",
            "            /* STACK LOW — trigger error handler */",
            "            Error_Handler();",
            "        }",
            "    }",
            "#endif",
        ]

    if cfg.heap.enabled:
        lines += [
            "#if SAFETY_HEAP_ENABLED",
            "    if (now_ms - last_heap_check >= SAFETY_HEAP_CHECK_MS) {",
            "        last_heap_check = now_ms;",
            "        /* Application-specific heap check */",
            "    }",
            "#endif",
        ]

    lines += [
        "}",
        "",
    ]

    return "\n".join(lines)


