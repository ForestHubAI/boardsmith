def _rp2040_impl(cfg: SafetyConfig) -> str:
    lines = [
        "/* boardsmith-fw safety monitor — RP2040/Pico SDK */",
        '#include "safety_config.h"',
        "",
        '#include "pico/stdlib.h"',
        '#include "hardware/watchdog.h"',
        "",
        "static uint32_t last_stack_check;",
        "static uint32_t last_heap_check;",
        "",
    ]

    # Stack check helper
    if cfg.stack.enabled:
        lines += [
            "/* Linker symbols */",
            "extern uint32_t __StackBottom;",
            "extern uint32_t __StackLimit;",
            "",
            "static uint32_t stack_check_free(void) {",
            "    volatile uint32_t *bottom = &__StackBottom;",
            "    uint32_t free_words = 0;",
            "    while (*bottom == SAFETY_STACK_PAINT) {",
            "        free_words++;",
            "        bottom++;",
            "    }",
            "    return free_words * 4;",
            "}",
            "",
        ]

    # safety_init
    lines += [
        "void safety_init(void) {",
    ]

    if cfg.watchdog.enabled:
        lines += [
            "#if SAFETY_WDT_ENABLED",
            "    watchdog_enable(SAFETY_WDT_TIMEOUT_MS, true);",
            '    printf("[safety] watchdog enabled: %dms\\n", SAFETY_WDT_TIMEOUT_MS);',
            "#endif",
        ]

    lines += [
        "    last_stack_check = 0;",
        "    last_heap_check = 0;",
        '    printf("[safety] safety monitor initialized\\n");',
        "}",
        "",
    ]

    # safety_wdt_feed
    lines += [
        "void safety_wdt_feed(void) {",
        "#if SAFETY_WDT_ENABLED",
        "    watchdog_update();",
        "#endif",
        "}",
        "",
    ]

    # safety_check — called from main loop
    lines += [
        "void safety_check(void) {",
        "    uint32_t now = to_ms_since_boot(get_absolute_time());",
    ]

    if cfg.stack.enabled:
        lines += [
            "#if SAFETY_STACK_ENABLED",
            "    if (now - last_stack_check >= SAFETY_STACK_CHECK_MS) {",
            "        last_stack_check = now;",
            "        uint32_t free = stack_check_free();",
            "        if (free < SAFETY_STACK_MIN_FREE) {",
            '            printf("[safety] STACK LOW: %u bytes\\n", free);',
            "        }",
            "    }",
            "#endif",
        ]

    if cfg.heap.enabled:
        lines += [
            "#if SAFETY_HEAP_ENABLED",
            "    if (now - last_heap_check >= SAFETY_HEAP_CHECK_MS) {",
            "        last_heap_check = now;",
            "        /* RP2040: no built-in heap stats — application-specific */",
            "    }",
            "#endif",
        ]

    lines += [
        "}",
        "",
    ]

    return "\n".join(lines)


