def _nrf52_impl(cfg: SafetyConfig) -> str:
    lines = [
        "/* boardsmith-fw safety monitor — nRF52/Zephyr */",
        '#include "safety_config.h"',
        "",
        '#include <zephyr/kernel.h>',
        '#include <zephyr/logging/log.h>',
    ]

    if cfg.watchdog.enabled:
        lines.append('#include <zephyr/drivers/watchdog.h>')

    lines += [
        "",
        'LOG_MODULE_REGISTER(safety, LOG_LEVEL_WRN);',
        "",
    ]

    if cfg.watchdog.enabled:
        lines += [
            "#if SAFETY_WDT_ENABLED",
            "static const struct device *wdt_dev;",
            "static int wdt_channel_id;",
            "#endif",
            "",
        ]

    # Monitor thread
    if cfg.stack.enabled or cfg.heap.enabled:
        lines += [
            "static void safety_monitor_entry(void *p1, void *p2, void *p3) {",
            "    ARG_UNUSED(p1); ARG_UNUSED(p2); ARG_UNUSED(p3);",
            "",
            "    for (;;) {",
        ]

        if cfg.stack.enabled:
            lines += [
                "#if SAFETY_STACK_ENABLED",
                "        size_t unused = 0;",
                "        k_thread_stack_space_get(k_current_get(), &unused);",
                "        if (unused < SAFETY_STACK_MIN_FREE) {",
                '            LOG_WRN("Stack low: %zu bytes remaining", unused);',
                "        }",
                "#endif",
            ]

        if cfg.heap.enabled:
            lines += [
                "#if SAFETY_HEAP_ENABLED",
                "        /* Zephyr: application-specific heap check */",
                "#endif",
            ]

        period = min(
            cfg.stack.check_interval_ms if cfg.stack.enabled else 99999,
            cfg.heap.check_interval_ms if cfg.heap.enabled else 99999,
        )
        lines += [
            f"        k_msleep({period});",
            "    }",
            "}",
            "",
            "K_THREAD_DEFINE(safety_mon, 1024, safety_monitor_entry, NULL, NULL, NULL, 14, 0, 0);",
            "",
        ]

    # safety_init
    lines += [
        "void safety_init(void) {",
    ]
    if cfg.watchdog.enabled:
        lines += [
            "#if SAFETY_WDT_ENABLED",
            '    wdt_dev = DEVICE_DT_GET(DT_ALIAS(watchdog0));',
            "    if (device_is_ready(wdt_dev)) {",
            "        struct wdt_timeout_cfg cfg = {",
            "            .window.max = SAFETY_WDT_TIMEOUT_MS,",
            "            .callback = NULL,",
            "            .flags = WDT_FLAG_RESET_SOC,",
            "        };",
            "        wdt_channel_id = wdt_install_timeout(wdt_dev, &cfg);",
            "        wdt_setup(wdt_dev, 0);",
            '        LOG_INF("Watchdog enabled: %dms", SAFETY_WDT_TIMEOUT_MS);',
            "    }",
            "#endif",
        ]
    lines += [
        '    LOG_INF("Safety monitor initialized");',
        "}",
        "",
    ]

    # safety_wdt_feed
    lines += [
        "void safety_wdt_feed(void) {",
        "#if SAFETY_WDT_ENABLED",
        "    if (wdt_dev && device_is_ready(wdt_dev)) {",
        "        wdt_feed(wdt_dev, wdt_channel_id);",
        "    }",
        "#endif",
        "}",
        "",
    ]

    return "\n".join(lines)


