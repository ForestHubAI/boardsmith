def _gen_esp32(cfg: SafetyConfig, result: SafetyCodegenResult) -> None:
    header = _esp32_header(cfg)
    impl = _esp32_impl(cfg)
    result.files.append(("main/safety_config.h", header))
    result.files.append(("main/safety_monitor.c", impl))


def _esp32_header(cfg: SafetyConfig) -> str:
    lines = [
        "/* boardsmith-fw safety config — ESP32/FreeRTOS */",
        "/* IEC 61508 inspired — generated by boardsmith-fw */",
        "#ifndef SAFETY_CONFIG_H",
        "#define SAFETY_CONFIG_H",
        "",
        "#include <stdbool.h>",
        "#include <stdint.h>",
        "",
        "/* Watchdog */",
        f"#define SAFETY_WDT_ENABLED       {_bool_c(cfg.watchdog.enabled)}",
        f"#define SAFETY_WDT_TIMEOUT_MS    {cfg.watchdog.timeout_ms}",
        f"#define SAFETY_WDT_PANIC         {_bool_c(cfg.watchdog.panic_on_trigger)}",
        "",
        "/* Stack overflow detection */",
        f"#define SAFETY_STACK_ENABLED     {_bool_c(cfg.stack.enabled)}",
        f"#define SAFETY_STACK_CHECK_MS    {cfg.stack.check_interval_ms}",
        f"#define SAFETY_STACK_MIN_FREE    {cfg.stack.min_free_bytes}",
        "",
        "/* Heap monitoring */",
        f"#define SAFETY_HEAP_ENABLED      {_bool_c(cfg.heap.enabled)}",
        f"#define SAFETY_HEAP_CHECK_MS     {cfg.heap.check_interval_ms}",
        f"#define SAFETY_HEAP_LOW_WATER    {cfg.heap.low_watermark_bytes}",
        "",
        "void safety_init(void);",
        "void safety_wdt_feed(void);",
        "",
        "#endif /* SAFETY_CONFIG_H */",
        "",
    ]
    return "\n".join(lines)


def _esp32_impl(cfg: SafetyConfig) -> str:
    lines = [
        "/* boardsmith-fw safety monitor — ESP32/FreeRTOS */",
        '#include "safety_config.h"',
        "",
        '#include "esp_log.h"',
        '#include "esp_system.h"',
        '#include "esp_task_wdt.h"',
        '#include "freertos/FreeRTOS.h"',
        '#include "freertos/task.h"',
        "",
        'static const char *TAG = "safety";',
        "",
    ]

    # Stack overflow hook
    if cfg.stack.enabled:
        lines += [
            "/* FreeRTOS stack overflow hook — called on overflow */",
            "void vApplicationStackOverflowHook(TaskHandle_t task, char *name) {",
            '    ESP_LOGE(TAG, "STACK OVERFLOW: task=%s", name);',
            "    esp_system_abort(\"stack overflow\");",
            "}",
            "",
        ]

    # Monitoring task
    if cfg.stack.enabled or cfg.heap.enabled:
        lines += [
            "static void safety_monitor_task(void *arg) {",
            "    (void)arg;",
            "",
        ]
        # Determine the longest interval so we can use it as the loop period
        intervals = []
        if cfg.stack.enabled:
            intervals.append(cfg.stack.check_interval_ms)
        if cfg.heap.enabled:
            intervals.append(cfg.heap.check_interval_ms)
        period = min(intervals) if intervals else 10000

        lines += [
            f"    const TickType_t period = pdMS_TO_TICKS({period});",
            "    TickType_t last_wake = xTaskGetTickCount();",
            "",
            "    for (;;) {",
        ]

        if cfg.stack.enabled:
            lines += [
                "#if SAFETY_STACK_ENABLED",
                "        {",
                "            UBaseType_t hwm = uxTaskGetStackHighWaterMark(NULL);",
                "            if (hwm < SAFETY_STACK_MIN_FREE) {",
                '                ESP_LOGW(TAG, "Stack low: %u bytes remaining", (unsigned)hwm);',
                "            }",
                "        }",
                "#endif",
                "",
            ]

        if cfg.heap.enabled:
            lines += [
                "#if SAFETY_HEAP_ENABLED",
                "        {",
                "            uint32_t free_heap = esp_get_free_heap_size();",
                "            if (free_heap < SAFETY_HEAP_LOW_WATER) {",
                '                ESP_LOGW(TAG, "Heap low: %lu bytes free", (unsigned long)free_heap);',
                "            }",
                "        }",
                "#endif",
                "",
            ]

        lines += [
            "        vTaskDelayUntil(&last_wake, period);",
            "    }",
            "}",
            "",
        ]

    # safety_init
    lines += [
        "void safety_init(void) {",
    ]

    if cfg.watchdog.enabled:
        timeout_s = max(1, cfg.watchdog.timeout_ms // 1000)
        lines += [
            "#if SAFETY_WDT_ENABLED",
            f"    esp_task_wdt_init({timeout_s}, SAFETY_WDT_PANIC);",
            "    esp_task_wdt_add(NULL);",
            '    ESP_LOGI(TAG, "Watchdog enabled: %ds timeout", '
            f"{timeout_s});",
            "#endif",
        ]

    if cfg.stack.enabled or cfg.heap.enabled:
        lines += [
            "",
            '    xTaskCreate(safety_monitor_task, "safety_mon", 2048, NULL, 1, NULL);',
            '    ESP_LOGI(TAG, "Safety monitor started");',
        ]

    lines += [
        "}",
        "",
    ]

    # safety_wdt_feed
    lines += [
        "void safety_wdt_feed(void) {",
        "#if SAFETY_WDT_ENABLED",
        "    esp_task_wdt_reset();",
        "#endif",
        "}",
        "",
    ]

    return "\n".join(lines)


