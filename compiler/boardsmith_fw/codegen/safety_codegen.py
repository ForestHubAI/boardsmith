"""Safety Template Code Generator — IEC 61508 inspired safety scaffolding.

Generates watchdog, stack-overflow detection, and heap monitoring code
for ESP32/FreeRTOS, STM32/HAL, and RP2040/Pico SDK targets.

    target + config → safety_config.h + safety_monitor.c + safety_summary.md

Usage:
    from boardsmith_fw.codegen.safety_codegen import generate_safety
    result = generate_safety(target="esp32")
"""

from __future__ import annotations

import re
from dataclasses import dataclass, field

from pydantic import BaseModel, Field

# ---------------------------------------------------------------------------
# Configuration model
# ---------------------------------------------------------------------------


class WatchdogConfig(BaseModel):
    """Watchdog timer configuration."""

    enabled: bool = True
    timeout_ms: int = 5000
    panic_on_trigger: bool = True


class StackConfig(BaseModel):
    """Stack overflow detection configuration."""

    enabled: bool = True
    check_interval_ms: int = 10000
    min_free_bytes: int = 256
    paint_pattern: int = 0xDEADBEEF


class HeapConfig(BaseModel):
    """Heap monitoring configuration."""

    enabled: bool = True
    check_interval_ms: int = 30000
    low_watermark_bytes: int = 4096


class SafetyConfig(BaseModel):
    """Top-level safety configuration."""

    watchdog: WatchdogConfig = Field(default_factory=WatchdogConfig)
    stack: StackConfig = Field(default_factory=StackConfig)
    heap: HeapConfig = Field(default_factory=HeapConfig)
    task_names: list[str] = Field(default_factory=list)


# ---------------------------------------------------------------------------
# Result
# ---------------------------------------------------------------------------


@dataclass
class SafetyCodegenResult:
    files: list[tuple[str, str]] = field(default_factory=list)
    warnings: list[str] = field(default_factory=list)


# ---------------------------------------------------------------------------
# Public API
# ---------------------------------------------------------------------------


def generate_safety(
    target: str = "esp32",
    config: SafetyConfig | None = None,
) -> SafetyCodegenResult:
    """Generate safety template files for the given target."""
    if config is None:
        config = SafetyConfig()

    result = SafetyCodegenResult()

    if target == "stm32":
        _gen_stm32(config, result)
    elif target == "rp2040":
        _gen_rp2040(config, result)
    elif target == "nrf52":
        _gen_nrf52(config, result)
    else:  # esp32, esp32c3
        _gen_esp32(config, result)

    result.files.append(("safety_summary.md", _gen_summary(config, target)))
    return result


