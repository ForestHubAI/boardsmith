def _gen_rp2040(cfg: OTAConfig, result: OTACodegenResult) -> None:
    result.files.append(("ota_config.h", _rp2040_header(cfg)))
    result.files.append(("ota_update.c", _rp2040_impl(cfg)))


def _rp2040_header(cfg: OTAConfig) -> str:
    lines = [
        "/* boardsmith-fw OTA config — RP2040/Pico SDK */",
        "/* Generated by boardsmith-fw */",
        "#ifndef OTA_CONFIG_H",
        "#define OTA_CONFIG_H",
        "",
        "#include <stdbool.h>",
        "#include <stdint.h>",
        "",
        f'#define OTA_FW_VERSION       "{_escape(cfg.firmware_version)}"',
        f"#define OTA_MAX_RETRIES      {cfg.max_retries}",
        "",
        "/* Flash layout */",
        "#define OTA_FLASH_OFFSET     (256 * 1024)  /* 256KB into flash */",
        "#define OTA_MAX_SIZE         (256 * 1024)  /* 256KB max firmware */",
        "#define OTA_SECTOR_SIZE      4096",
        "",
        "typedef enum {",
        "    OTA_OK = 0,",
        "    OTA_ERR_ERASE,",
        "    OTA_ERR_WRITE,",
        "    OTA_ERR_VERIFY,",
        "    OTA_ERR_SIZE,",
        "} ota_err_t;",
        "",
        "void ota_init(void);",
        "ota_err_t ota_write_chunk(uint32_t offset, const uint8_t *data, uint32_t len);",
        "ota_err_t ota_finish_and_reboot(void);",
        "",
        "#endif /* OTA_CONFIG_H */",
        "",
    ]
    return "\n".join(lines)


def _rp2040_impl(cfg: OTAConfig) -> str:
    lines = [
        "/* boardsmith-fw OTA update — RP2040/Pico SDK flash */",
        '#include "ota_config.h"',
        "",
        '#include "hardware/flash.h"',
        '#include "hardware/watchdog.h"',
        '#include "pico/stdlib.h"',
        "",
        '#include <string.h>',
        "",
        "static uint32_t ota_bytes_written;",
        "",
        "void ota_init(void) {",
        "    ota_bytes_written = 0;",
        '    printf("[ota] OTA ready — version: %s\\n", OTA_FW_VERSION);',
        "}",
        "",
        "ota_err_t ota_write_chunk(uint32_t offset, const uint8_t *data, uint32_t len) {",
        "    if (offset + len > OTA_MAX_SIZE) {",
        "        return OTA_ERR_SIZE;",
        "    }",
        "",
        "    uint32_t flash_addr = OTA_FLASH_OFFSET + offset;",
        "",
        "    /* Erase sectors as needed */",
        "    if (offset % OTA_SECTOR_SIZE == 0) {",
        "        uint32_t erase_len = ((len + OTA_SECTOR_SIZE - 1) / OTA_SECTOR_SIZE) * OTA_SECTOR_SIZE;",
        "        flash_range_erase(flash_addr, erase_len);",
        "    }",
        "",
        "    /* Write in 256-byte pages */",
        "    flash_range_program(flash_addr, data, len);",
        "    ota_bytes_written += len;",
        "",
        "    return OTA_OK;",
        "}",
        "",
        "ota_err_t ota_finish_and_reboot(void) {",
        "    /* Verify: check first bytes are not erased */",
        "    const uint8_t *check = (const uint8_t *)(XIP_BASE + OTA_FLASH_OFFSET);",
        "    if (check[0] == 0xFF && check[1] == 0xFF) {",
        "        return OTA_ERR_VERIFY;",
        "    }",
        "",
        '    printf("[ota] Update complete (%u bytes) — rebooting\\n", ota_bytes_written);',
        "    watchdog_reboot(0, 0, 100);",
        "    return OTA_OK;  /* unreachable */",
        "}",
        "",
    ]
    return "\n".join(lines)


