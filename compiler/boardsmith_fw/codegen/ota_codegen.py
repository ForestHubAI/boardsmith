def _nrf52_impl(cfg: OTAConfig) -> str:
    lines = [
        "/* boardsmith-fw OTA update — nRF52/Zephyr MCUboot */",
        '#include "ota_config.h"',
        "",
        '#include <zephyr/kernel.h>',
        '#include <zephyr/dfu/mcuboot.h>',
        '#include <zephyr/logging/log.h>',
        "",
        'LOG_MODULE_REGISTER(ota, LOG_LEVEL_INF);',
        "",
        "void ota_init(void) {",
        '    LOG_INF("OTA init — firmware version: %s", OTA_FW_VERSION);',
        "",
    ]

    if cfg.rollback_enabled:
        lines += [
            "#if OTA_ROLLBACK_ENABLED",
            "    if (!boot_is_img_confirmed()) {",
            '        LOG_INF("Confirming current image");',
            "        boot_write_img_confirmed();",
            "    }",
            "#endif",
        ]

    lines += [
        "}",
        "",
        "ota_err_t ota_confirm_image(void) {",
        "    int rc = boot_write_img_confirmed();",
        "    if (rc != 0) {",
        '        LOG_ERR("Failed to confirm image: %d", rc);',
        "        return OTA_ERR_CONFIRM;",
        "    }",
        '    LOG_INF("Image confirmed");',
        "    return OTA_OK;",
        "}",
        "",
        "ota_err_t ota_request_upgrade(void) {",
        "    int rc = boot_request_upgrade(BOOT_UPGRADE_TEST);",
        "    if (rc != 0) {",
        '        LOG_ERR("Failed to request upgrade: %d", rc);',
        "        return OTA_ERR_FLASH;",
        "    }",
        '    LOG_INF("Upgrade requested — reboot to apply");',
        "    return OTA_OK;",
        "}",
        "",
    ]
    return "\n".join(lines)


# ---------------------------------------------------------------------------
# Summary
# ---------------------------------------------------------------------------


def _gen_summary(cfg: OTAConfig, target: str) -> str:
    lines = [
        "# OTA Update Configuration Summary",
        "",
        f"Target: **{target}**",
        f"Firmware version: `{cfg.firmware_version}`",
        "",
    ]

    if target == "esp32":
        lines += [
            "## Architecture: ESP32 Dual-Bank OTA",
            "",
            "- Method: `esp_https_ota` with partition table",
            f"- Server: `{cfg.server_url}`",
            f"- Check interval: {cfg.check_interval_s}s",
            f"- Max retries: {cfg.max_retries}",
            f"- Signature verification: {cfg.verify_signature}",
            f"- Rollback support: {cfg.rollback_enabled}",
            "",
            "### Flow",
            "",
            "1. Background task periodically checks server for updates",
            "2. Downloads firmware to inactive OTA partition",
            "3. Verifies integrity and reboots to new partition",
            "4. On next boot, marks firmware valid (anti-rollback)",
            "",
        ]
    elif target == "stm32":
        lines += [
            "## Architecture: STM32 Single-Bank Flash OTA",
            "",
            "- Method: HAL flash write to application slot B",
            "- Flash address: `0x08020000` (128KB slot)",
            f"- Max retries: {cfg.max_retries}",
            "",
            "### Flow",
            "",
            "1. Receive firmware chunks via UART/SPI/I2C",
            "2. Write chunks to flash slot B with `ota_write_chunk()`",
            "3. Verify and reboot with `ota_finish_and_reboot()`",
            "",
        ]
    elif target == "rp2040":
        lines += [
            "## Architecture: RP2040 Flash OTA",
            "",
            "- Method: Pico SDK `flash_range_program`",
            "- Flash offset: 256KB",
            "- Max firmware size: 256KB",
            f"- Max retries: {cfg.max_retries}",
            "",
            "### Flow",
            "",
            "1. Receive firmware chunks via USB/UART",
            "2. Erase and write flash with `ota_write_chunk()`",
            "3. Verify and reboot with `ota_finish_and_reboot()`",
            "",
        ]

    elif target == "nrf52":
        lines += [
            "## Architecture: nRF52 MCUboot DFU",
            "",
            "- Method: MCUboot bootloader + Zephyr DFU API",
            f"- Max retries: {cfg.max_retries}",
            f"- Rollback support: {cfg.rollback_enabled}",
            "",
            "### Flow",
            "",
            "1. Receive firmware image via BLE DFU / UART",
            "2. Write to secondary slot (managed by MCUboot)",
            "3. Request upgrade with `boot_request_upgrade()`",
            "4. MCUboot swaps images on reboot",
            "5. Confirm new image with `boot_write_img_confirmed()`",
            "",
        ]

    lines += [
        "## Files Generated",
        "",
    ]

    if target in ("esp32", "esp32c3"):
        lines += [
            "- `main/ota_config.h` — OTA defines and API",
            "- `main/ota_update.c` — esp_https_ota implementation",
        ]
    elif target == "stm32":
        lines += [
            "- `Src/ota_config.h` — OTA defines, error codes, API",
            "- `Src/ota_update.c` — HAL flash write implementation",
        ]
    elif target == "rp2040":
        lines += [
            "- `ota_config.h` — OTA defines, error codes, API",
            "- `ota_update.c` — Pico SDK flash write implementation",
        ]
    elif target == "nrf52":
        lines += [
            "- `src/ota_config.h` — OTA defines, error codes, API",
            "- `src/ota_update.c` — MCUboot DFU implementation",
        ]

    lines += [
        "- `ota_summary.md` — This file",
        "",
    ]

    return "\n".join(lines)


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _bool_c(val: bool) -> str:
    return "1" if val else "0"


def _escape(text: str) -> str:
    return text.replace('"', '\\"').replace("\\", "\\\\")


def _sanitize(name: str) -> str:
    return re.sub(r"[^a-zA-Z0-9_]", "_", name).strip("_")
