"""OTA Update Scaffolding Code Generator.

Generates over-the-air update support code for ESP32, STM32, and RP2040 targets.

    target + config → ota_config.h + ota_update.c + ota_summary.md

ESP32:  esp_https_ota + partition-based dual-bank OTA
STM32:  HAL flash write + application jump (single-bank basic OTA)
RP2040: Pico SDK flash write + reboot

Usage:
    from boardsmith_fw.codegen.ota_codegen import generate_ota
    result = generate_ota(target="esp32")
"""

from __future__ import annotations

import re
from dataclasses import dataclass, field

from pydantic import BaseModel

# ---------------------------------------------------------------------------
# Configuration model
# ---------------------------------------------------------------------------


class OTAConfig(BaseModel):
    """OTA update configuration."""

    server_url: str = "https://firmware.example.com/ota"
    check_interval_s: int = 3600
    max_retries: int = 3
    verify_signature: bool = True
    rollback_enabled: bool = True
    firmware_version: str = "1.0.0"


# ---------------------------------------------------------------------------
# Result
# ---------------------------------------------------------------------------


@dataclass
class OTACodegenResult:
    files: list[tuple[str, str]] = field(default_factory=list)
    warnings: list[str] = field(default_factory=list)


# ---------------------------------------------------------------------------
# Public API
# ---------------------------------------------------------------------------


def generate_ota(
    target: str = "esp32",
    config: OTAConfig | None = None,
) -> OTACodegenResult:
    """Generate OTA update scaffolding for the given target."""
    if config is None:
        config = OTAConfig()

    result = OTACodegenResult()

    if target == "stm32":
        _gen_stm32(config, result)
    elif target == "rp2040":
        _gen_rp2040(config, result)
    elif target == "nrf52":
        _gen_nrf52(config, result)
    else:  # esp32, esp32c3
        _gen_esp32(config, result)

    result.files.append(("ota_summary.md", _gen_summary(config, target)))
    return result


# ---------------------------------------------------------------------------
# ESP32 — esp_https_ota with dual-bank partition
# ---------------------------------------------------------------------------


def _gen_esp32(cfg: OTAConfig, result: OTACodegenResult) -> None:
    result.files.append(("main/ota_config.h", _esp32_header(cfg)))
    result.files.append(("main/ota_update.c", _esp32_impl(cfg)))


def _esp32_header(cfg: OTAConfig) -> str:
    lines = [
        "/* boardsmith-fw OTA config — ESP32/esp_https_ota */",
        "/* Generated by boardsmith-fw */",
        "#ifndef OTA_CONFIG_H",
        "#define OTA_CONFIG_H",
        "",
        "#include <stdbool.h>",
        "#include <stdint.h>",
        "",
        f'#define OTA_SERVER_URL       "{_escape(cfg.server_url)}"',
        f"#define OTA_CHECK_INTERVAL_S {cfg.check_interval_s}",
        f"#define OTA_MAX_RETRIES      {cfg.max_retries}",
        f"#define OTA_VERIFY_SIGNATURE {_bool_c(cfg.verify_signature)}",
        f"#define OTA_ROLLBACK_ENABLED {_bool_c(cfg.rollback_enabled)}",
        f'#define OTA_FW_VERSION       "{_escape(cfg.firmware_version)}"',
        "",
        "void ota_init(void);",
        "void ota_check_and_update(void);",
        "",
        "#endif /* OTA_CONFIG_H */",
        "",
    ]
    return "\n".join(lines)


def _esp32_impl(cfg: OTAConfig) -> str:
    lines = [
        "/* boardsmith-fw OTA update — ESP32/esp_https_ota */",
        '#include "ota_config.h"',
        "",
        '#include "esp_http_client.h"',
        '#include "esp_https_ota.h"',
        '#include "esp_log.h"',
        '#include "esp_ota_ops.h"',
        '#include "esp_system.h"',
        '#include "freertos/FreeRTOS.h"',
        '#include "freertos/task.h"',
        "",
        'static const char *TAG = "ota";',
        "",
        "static void ota_task(void *arg);",
        "",
    ]

    # ota_init
    lines += [
        "void ota_init(void) {",
        '    ESP_LOGI(TAG, "OTA init — firmware version: %s", OTA_FW_VERSION);',
        "",
    ]
    if cfg.rollback_enabled:
        lines += [
            "#if OTA_ROLLBACK_ENABLED",
            "    const esp_partition_t *running = esp_ota_get_running_partition();",
            "    esp_ota_img_states_t state;",
            "    if (esp_ota_get_state_partition(running, &state) == ESP_OK) {",
            "        if (state == ESP_OTA_IMG_PENDING_VERIFY) {",
            '            ESP_LOGI(TAG, "OTA: confirming new firmware");',
            "            esp_ota_mark_app_valid_cancel_rollback();",
            "        }",
            "    }",
            "#endif",
            "",
        ]
    lines += [
        '    xTaskCreate(ota_task, "ota_task", 8192, NULL, 3, NULL);',
        "}",
        "",
    ]

    # ota_check_and_update
    lines += [
        "void ota_check_and_update(void) {",
        '    ESP_LOGI(TAG, "Checking for OTA update at: %s", OTA_SERVER_URL);',
        "",
        "    esp_http_client_config_t http_cfg = {",
        "        .url = OTA_SERVER_URL,",
        "        .timeout_ms = 10000,",
        "    };",
        "",
        "    esp_https_ota_config_t ota_cfg = {",
        "        .http_config = &http_cfg,",
        "    };",
        "",
        "    for (int attempt = 0; attempt < OTA_MAX_RETRIES; attempt++) {",
        "        esp_err_t err = esp_https_ota(&ota_cfg);",
        "        if (err == ESP_OK) {",
        '            ESP_LOGI(TAG, "OTA succeeded — rebooting");',
        "            esp_restart();",
        "        }",
        '        ESP_LOGW(TAG, "OTA attempt %d failed: %s", attempt + 1, esp_err_to_name(err));',
        "        vTaskDelay(pdMS_TO_TICKS(5000));",
        "    }",
        "",
        '    ESP_LOGE(TAG, "OTA failed after %d attempts", OTA_MAX_RETRIES);',
        "}",
        "",
    ]

    # Background task
    lines += [
        "static void ota_task(void *arg) {",
        "    (void)arg;",
        "    const TickType_t interval = pdMS_TO_TICKS(OTA_CHECK_INTERVAL_S * 1000);",
        "",
        "    /* Initial delay before first check */",
        "    vTaskDelay(pdMS_TO_TICKS(30000));",
        "",
        "    for (;;) {",
        "        ota_check_and_update();",
        "        vTaskDelay(interval);",
        "    }",
        "}",
        "",
    ]

    return "\n".join(lines)


