# STM32 — HAL flash write, basic single-bank OTA
# ---------------------------------------------------------------------------


def _gen_stm32(cfg: OTAConfig, result: OTACodegenResult) -> None:
    result.files.append(("Src/ota_config.h", _stm32_header(cfg)))
    result.files.append(("Src/ota_update.c", _stm32_impl(cfg)))


def _stm32_header(cfg: OTAConfig) -> str:
    lines = [
        "/* boardsmith-fw OTA config — STM32/HAL */",
        "/* Generated by boardsmith-fw */",
        "#ifndef OTA_CONFIG_H",
        "#define OTA_CONFIG_H",
        "",
        "#include <stdbool.h>",
        "#include <stdint.h>",
        "",
        f'#define OTA_FW_VERSION       "{_escape(cfg.firmware_version)}"',
        f"#define OTA_MAX_RETRIES      {cfg.max_retries}",
        f"#define OTA_VERIFY_SIGNATURE {_bool_c(cfg.verify_signature)}",
        "",
        "/* Flash layout */",
        "#define OTA_APP_ADDR         0x08020000u  /* Application slot B */",
        "#define OTA_APP_SIZE         0x00020000u  /* 128KB */",
        "",
        "typedef enum {",
        "    OTA_OK = 0,",
        "    OTA_ERR_ERASE,",
        "    OTA_ERR_WRITE,",
        "    OTA_ERR_VERIFY,",
        "    OTA_ERR_TIMEOUT,",
        "} ota_err_t;",
        "",
        "void ota_init(void);",
        "ota_err_t ota_write_chunk(uint32_t offset, const uint8_t *data, uint32_t len);",
        "ota_err_t ota_finish_and_reboot(void);",
        "",
        "#endif /* OTA_CONFIG_H */",
        "",
    ]
    return "\n".join(lines)


def _stm32_impl(cfg: OTAConfig) -> str:
    lines = [
        "/* boardsmith-fw OTA update — STM32/HAL flash write */",
        '#include "ota_config.h"',
        '#include "stm32f4xx_hal.h"',
        "",
        '#include <string.h>',
        "",
        "static uint32_t ota_bytes_written;",
        "",
        "void ota_init(void) {",
        "    ota_bytes_written = 0;",
        "    HAL_FLASH_Unlock();",
        "",
        "    /* Erase application slot B */",
        "    FLASH_EraseInitTypeDef erase = {",
        "        .TypeErase = FLASH_TYPEERASE_SECTORS,",
        "        .Sector = FLASH_SECTOR_5,  /* Adjust for your layout */",
        "        .NbSectors = 1,",
        "        .VoltageRange = FLASH_VOLTAGE_RANGE_3,",
        "    };",
        "    uint32_t error = 0;",
        "    HAL_FLASHEx_Erase(&erase, &error);",
        "    HAL_FLASH_Lock();",
        "}",
        "",
        "ota_err_t ota_write_chunk(uint32_t offset, const uint8_t *data, uint32_t len) {",
        "    HAL_FLASH_Unlock();",
        "",
        "    for (uint32_t i = 0; i < len; i++) {",
        "        if (HAL_FLASH_Program(FLASH_TYPEPROGRAM_BYTE,",
        "                              OTA_APP_ADDR + offset + i,",
        "                              data[i]) != HAL_OK) {",
        "            HAL_FLASH_Lock();",
        "            return OTA_ERR_WRITE;",
        "        }",
        "    }",
        "",
        "    ota_bytes_written += len;",
        "    HAL_FLASH_Lock();",
        "    return OTA_OK;",
        "}",
        "",
        "ota_err_t ota_finish_and_reboot(void) {",
        "    /* Verify: read back first word */",
        "    uint32_t first_word = *(volatile uint32_t *)OTA_APP_ADDR;",
        "    if (first_word == 0xFFFFFFFF) {",
        "        return OTA_ERR_VERIFY;",
        "    }",
        "",
        "    /* System reset to boot into new firmware */",
        "    HAL_NVIC_SystemReset();",
        "    return OTA_OK;  /* unreachable */",
        "}",
        "",
    ]
    return "\n".join(lines)


