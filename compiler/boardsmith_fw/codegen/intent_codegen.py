# ESP32 (FreeRTOS) generator
# ---------------------------------------------------------------------------

def _gen_esp32(name: str, periodic: list[IntentTask], triggered: list[IntentTask]) -> str:
    safe_name = _sanitize(name)
    lines = [
        f"/* boardsmith-fw intent: {name} — ESP32/FreeRTOS */",
        "/* Generated by boardsmith-fw intent compiler */",
        "/* Edit intent.yaml to change behaviour, not this file */",
        "",
        '#include "freertos/FreeRTOS.h"',
        '#include "freertos/task.h"',
        '#include "esp_log.h"',
        '#include "drivers.h"  /* HIR-generated driver layer */',
        "",
        f'static const char *TAG = "{safe_name}";',
        "",
    ]

    # Data structs for read tasks
    for task in periodic:
        for read in task.read:
            struct_name = _sanitize(read.store_as or read.component.lower())
            lines += _emit_data_struct(struct_name, read.values)

    # Handler prototypes for triggered tasks
    for task in triggered:
        lines.append(f"static void {_sanitize(task.name)}_handler(void *data);")
    if triggered:
        lines.append("")

    # Periodic task functions
    for task in periodic:
        lines += _emit_esp32_task(task, triggered)

    # Handler implementations for triggered tasks
    for task in triggered:
        lines += _emit_esp32_handler(task)

    # app_main
    lines += [
        "void app_main(void) {",
        f'    ESP_LOGI(TAG, "Starting {name}");',
        "    drivers_init();  /* HIR-generated init sequence */",
        "",
    ]
    for task in periodic:
        task_fn = f"{_sanitize(task.name)}_task"
        stack = 4096
        lines.append(f'    xTaskCreate({task_fn}, "{task.name}", {stack}, NULL, 5, NULL);')

    lines += [
        "}",
        "",
    ]

    return "\n".join(lines)


def _emit_data_struct(name: str, values: list[str]) -> list[str]:
    if not values:
        return []
    lines = ["typedef struct {"]
    for v in values:
        lines.append(f"    float {_sanitize(v)};")
    lines += [f"}} {name}_t;", ""]
    return lines


def _emit_esp32_task(task: IntentTask, triggered: list[IntentTask]) -> list[str]:
    fn_name = f"{_sanitize(task.name)}_task"
    delay_ms = task.every_ms()
    lines = [
        f"static void {fn_name}(void *arg) {{",
        f"    const TickType_t period = pdMS_TO_TICKS({delay_ms});",
        "    TickType_t last_wake = xTaskGetTickCount();",
        "",
        "    for (;;) {",
    ]

    # Read actions
    for read in task.read:
        struct_name = _sanitize(read.store_as or read.component.lower())
        cmp = _sanitize(read.component.lower())
        lines.append(f"        {struct_name}_t {struct_name} = {{0}};")
        for val in read.values:
            lines.append(f"        {struct_name}.{_sanitize(val)} = {cmp}_read_{_sanitize(val)}();")
        lines.append(f'        ESP_LOGI(TAG, "{task.name}: read {read.component}");')

    # Other actions
    for action in task.parsed_actions():
        lines += _emit_action_esp32(action, task)

    # Trigger downstream tasks (call their handlers directly)
    downstream = [t for t in triggered if t.trigger == task.name]
    for dep in downstream:
        for read in task.read:
            struct_name = _sanitize(read.store_as or read.component.lower())
            lines.append(f"        {_sanitize(dep.name)}_handler(&{struct_name});")

    lines += [
        "",
        "        vTaskDelayUntil(&last_wake, period);",
        "    }",
        "}",
        "",
    ]
    return lines


def _emit_esp32_handler(task: IntentTask) -> list[str]:
    """Emit a handler function body for a triggered task."""
    fn = f"{_sanitize(task.name)}_handler"
    lines = [
        f"static void {fn}(void *data) {{",
        "    (void)data;",
    ]
    for action in task.parsed_actions():
        lines += [f"    {ln}" for ln in _emit_action_esp32(action, task)]
    lines += ["}", ""]
    return lines


def _emit_action_esp32(action, task: IntentTask) -> list[str]:
    a = action.action
    args = action.args

    if a == "serial.print":
        text = args.get("text", "")
        return [f'        ESP_LOGI(TAG, "{_escape(text)}");']

    if a == "flash.append":
        device = args.get("device", "flash")
        data = args.get("data", "data")
        fmt = args.get("format", "binary")
        return [
            f"        /* flash.append: {device} fmt={fmt} */",
            f"        flash_append(&{_sanitize(data)}, sizeof({_sanitize(data)}));",
        ]

    if a == "display.show":
        device = args.get("device", "display")
        text = args.get("text", "")
        return [
            f"        /* display.show: {device} */",
            f'        display_print("{_escape(text)}");',
        ]

    if a == "mqtt.publish":
        topic = args.get("topic", "data")
        data = args.get("data", "data")
        return [
            f'        mqtt_publish("{_escape(topic)}", &{_sanitize(data)}, sizeof({_sanitize(data)}));',
        ]

    if a == "gpio.set":
        pin = args.get("pin", "0")
        value = args.get("value", "1")
        return [f"        gpio_set_level(GPIO_NUM_{pin}, {value});"]

    if a == "led.set":
        led_id = args.get("id", "0")
        value = args.get("value", "1")
        return [f"        led_set({led_id}, {value});"]

    if a == "ble.notify":
        uuid = args.get("uuid", "0")
        data = args.get("data", "data")
        return [f"        ble_notify_char({uuid}, &{_sanitize(data)}, sizeof({_sanitize(data)}));"]

    if a in ("http.get", "http.post"):
        url = args.get("url", "")
        method = "GET" if a == "http.get" else "POST"
        return [
            f"        /* HTTP {method}: {_escape(url)} */",
            f'        http_{method.lower()}("{_escape(url)}", NULL, 0, NULL, 0);',
        ]

    if a == "i2c.write":
        addr = args.get("addr", "0x00")
        data = args.get("data", "data")
        return [f"        i2c_write_to({addr}, &{_sanitize(data)}, sizeof({_sanitize(data)}));"]

    if a == "spi.transfer":
        cs = args.get("cs", "GPIO_NUM_5")
        data = args.get("data", "data")
        return [f"        spi_transfer({cs}, &{_sanitize(data)}, sizeof({_sanitize(data)}));"]

    # Unknown action — emit a descriptive comment, no TODO
    return [f"        /* Unimplemented action: '{a}' — add custom code here */"]


