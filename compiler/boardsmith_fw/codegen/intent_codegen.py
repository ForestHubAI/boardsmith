"""Intent Code Generator — compiles IntentRoot into C application loop.

Translates declarative firmware intent (what to do) into concrete C code
that runs on top of the HIR-generated driver layer.

    IntentRoot + target → main_intent.c
"""

from __future__ import annotations

import re
from dataclasses import dataclass

import yaml

from boardsmith_fw.models.intent_schema import IntentRoot, IntentTask


@dataclass
class IntentCodegenResult:
    files: list[tuple[str, str]]  # [(path, content)]
    warnings: list[str]


# ---------------------------------------------------------------------------
# Public API
# ---------------------------------------------------------------------------

def compile_intent(intent_yaml: str, target: str = "esp32") -> IntentCodegenResult:
    """Compile an intent YAML string into C source files."""
    raw = yaml.safe_load(intent_yaml)
    root = IntentRoot(**raw)
    return _generate(root, target)


def compile_intent_from_file(path, target: str = "esp32") -> IntentCodegenResult:
    """Compile an intent YAML file into C source files."""
    from pathlib import Path
    text = Path(path).read_text(encoding="utf-8")
    return compile_intent(text, target)


# ---------------------------------------------------------------------------
# Generator
# ---------------------------------------------------------------------------

def _generate(root: IntentRoot, target: str) -> IntentCodegenResult:
    fw = root.firmware
    warnings: list[str] = []

    if not fw.tasks:
        warnings.append("No tasks defined in intent — generating empty main loop")

    # Separate periodic tasks from triggered tasks
    periodic = [t for t in fw.tasks if t.every and not t.trigger]
    triggered = [t for t in fw.tasks if t.trigger]

    if target in ("stm32",):
        content = _gen_stm32(fw.name, periodic, triggered)
    elif target in ("rp2040",):
        content = _gen_rp2040(fw.name, periodic, triggered)
    elif target in ("nrf52",):
        content = _gen_nrf52(fw.name, periodic, triggered)
    else:  # esp32/esp32c3 default
        content = _gen_esp32(fw.name, periodic, triggered)

    files = [("main_intent.c", content)]

    # Also emit a human-readable intent summary as a comment file
    summary = _gen_summary(root)
    files.append(("intent_summary.md", summary))

    return IntentCodegenResult(files=files, warnings=warnings)


