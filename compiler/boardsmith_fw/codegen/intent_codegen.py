# STM32 (HAL + bare-metal super-loop) generator
# ---------------------------------------------------------------------------

def _gen_stm32(name: str, periodic: list[IntentTask], triggered: list[IntentTask]) -> str:
    safe_name = _sanitize(name)
    lines = [
        f"/* boardsmith-fw intent: {name} — STM32/HAL */",
        "/* Generated by boardsmith-fw intent compiler */",
        "",
        '#include "main.h"',
        '#include "stm32f4xx_hal.h"',
        '#include "drivers.h"',
        "",
        f"/* {safe_name} application loop */",
        "",
    ]

    # Data structs
    for task in periodic:
        for read in task.read:
            struct_name = _sanitize(read.store_as or read.component.lower())
            lines += _emit_data_struct(struct_name, read.values)

    # Forward declarations
    for task in periodic:
        lines += [f"static void run_{_sanitize(task.name)}(void);", ""]
    for task in triggered:
        lines += [f"static void {_sanitize(task.name)}_handler(void *data);", ""]
    lines.append("")

    lines += [
        "void app_run(void) {",
        "    drivers_init();",
        "",
    ]
    for task in periodic:
        lines.append(f"    uint32_t tick_{_sanitize(task.name)} = 0;")

    lines += [
        "",
        "    for (;;) {",
        "        uint32_t now = HAL_GetTick();",
        "",
    ]
    for task in periodic:
        delay_ms = task.every_ms()
        fn = f"run_{_sanitize(task.name)}"
        t = f"tick_{_sanitize(task.name)}"
        lines += [
            f"        if (now - {t} >= {delay_ms}u) {{",
            f"            {t} = now;",
            f"            {fn}();",
            "        }",
            "",
        ]
    lines += ["    }", "}", ""]

    # Task implementations
    for task in periodic:
        fn = f"run_{_sanitize(task.name)}"
        lines += [f"static void {fn}(void) {{"]
        for read in task.read:
            struct_name = _sanitize(read.store_as or read.component.lower())
            cmp = _sanitize(read.component.lower())
            lines.append(f"    {struct_name}_t {struct_name} = {{0}};")
            for val in read.values:
                lines.append(f"    {struct_name}.{_sanitize(val)} = {cmp}_read_{_sanitize(val)}();")
        for action in task.parsed_actions():
            for line in _emit_action_stm32(action):
                lines.append(f"    {line}")
        downstream = [t for t in triggered if t.trigger == task.name]
        for dep in downstream:
            for read in task.read:
                struct_name = _sanitize(read.store_as or read.component.lower())
                lines.append(f"    {_sanitize(dep.name)}_handler(&{struct_name});")
        lines += ["}", ""]

    # Handler implementations for triggered tasks
    for task in triggered:
        fn = f"{_sanitize(task.name)}_handler"
        lines += [f"static void {fn}(void *data) {{", "    (void)data;"]
        for action in task.parsed_actions():
            for line in _emit_action_stm32(action):
                lines.append(f"    {line}")
        lines += ["}", ""]

    return "\n".join(lines)


def _emit_action_stm32(action) -> list[str]:
    a = action.action
    args = action.args
    if a == "serial.print":
        text = args.get("text", "")
        return [f'HAL_UART_Transmit(&huart2, (uint8_t*)"{_escape(text)}\\r\\n", '
                f'strlen("{_escape(text)}") + 2, 100);']
    if a == "flash.append":
        data = args.get("data", "data")
        return [f"flash_append(&{_sanitize(data)}, sizeof({_sanitize(data)}));"]
    if a == "display.show":
        text = args.get("text", "")
        return [f'display_print("{_escape(text)}");']
    if a == "mqtt.publish":
        topic = args.get("topic", "data")
        data = args.get("data", "data")
        return [f'mqtt_publish("{_escape(topic)}", &{_sanitize(data)}, sizeof({_sanitize(data)}));']
    if a == "gpio.set":
        pin = args.get("pin", "0")
        value = args.get("value", "1")
        return [f"HAL_GPIO_WritePin(GPIOA, GPIO_PIN_{pin}, GPIO_PIN_{'SET' if value == '1' else 'RESET'});"]
    if a == "led.set":
        led_id = args.get("id", "0")
        value = args.get("value", "1")
        return [f"led_set({led_id}, {value});"]
    if a == "ble.notify":
        uuid = args.get("uuid", "0")
        data = args.get("data", "data")
        return [f"ble_notify_char({uuid}, &{_sanitize(data)}, sizeof({_sanitize(data)}));"]
    # Unknown action — descriptive comment, no TODO
    return [f"/* Unimplemented action: '{a}' — add custom code here */"]


